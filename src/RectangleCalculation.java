
public class RectangleCalculation {

	public static void main(String[] args) {
		
         int length = 5;
         int breadth = 8;

		 int perimeter = 2*(length + breadth);
					
		 int area = length * breadth;			
					
		System.out.println("Perimeter formula P=2(length + breadth) = " + perimeter);
		
		System.out.println("Area of a Rectangle Area = (length X breadth) = " + area);
			    
			
    }

}

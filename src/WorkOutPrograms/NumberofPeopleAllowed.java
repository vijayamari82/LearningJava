package WorkOutPrograms;

public class NumberofPeopleAllowed {

public static void main(String[] args)
		{

		int maxCapacity =50;
        int currentCapacity=10;
        int allowCapacity=5;

         while (currentCapacity<maxCapacity)
	        {

               System.out.println("Current Capacity of People = " + currentCapacity);
               if(allowCapacity == allowCapacity)

                {

                 currentCapacity=currentCapacity+allowCapacity;

                }

            }

       System.out.println("Maximum No. of People Entered = " + currentCapacity);                      

 }

}		 

